package 증감연산자;

public class Ex01증감연산자 {

	public static void main(String[] args) {
		
		int num = 3;
		
		System.out.println(num);
		System.out.println(++num);
		System.out.println(num++);
		System.out.println(num);
		
		int opr = 0;
		opr += 3;
		System.out.println(opr++);
		System.out.println(opr);
		System.out.println(++opr);
		System.out.println(opr);
		System.out.println(opr--);
		System.out.println(opr);
		System.out.println(--opr);
		System.out.println(opr);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
